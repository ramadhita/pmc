float plus (float a,float b){
    float sum;
    sum = a+b;
    return(sum);
};

float minus (float c,float d){
    float diff;
    diff = c - d;
    return(diff);
};

float times (float e,float f){
    float total;
    total = e*f;
    return(total);
};

float divide (float g, float h){
    float divi;
    divi = g/h ;
    return(divi);
}